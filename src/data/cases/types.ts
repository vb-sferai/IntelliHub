/**
 * Результат/метрика в кейсе
 */
export interface CaseResult {
  /** Значение метрики (например: "90", "11 000", "85%") */
  title: string;
  /** Описание того, что означает эта метрика */
  text: string;
}

/**
 * Структура данных кейса
 */
export interface CaseStudy {
  /** Уникальный идентификатор */
  id: string;
  /** Название клиента */
  client: string;
  /** URL логотипа клиента (опционально) */
  logoUrl?: string;
  /** Краткое описание запроса/задачи клиента */
  request: string;
  /** Достигнутые результаты */
  results: CaseResult[];
  /** Использованный стек технологий */
  stack: string;
  /** Ссылка на детальную страницу кейса */
  link?: string;
}

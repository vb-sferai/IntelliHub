// Типы данных для страницы /automations

export interface Service {
    title: string;
    description: string;
    icon: 'Bot' | 'Search' | 'BarChart3' | 'Wand2' | 'Plug' | 'MessageSquare';
    isCTA?: boolean;
}

export interface RoadmapStep {
    number: number;
    title: string;
    description: string;
}

// Расширенный интерфейс для карточек кейсов (дизайн из PDF)
export interface CaseMetric {
    value: string; // "-75%", "10x", "1000"
    label: string; // "сокращение времени поиска"
}

export interface CaseClient {
    isNDA: boolean;
    name?: string; // "STONE" | "ПИК" | undefined для NDA
    logo?: string; // Путь к логотипу (для публичных клиентов)
    industry: string; // "Девелопер недвижимости" | "EdTech компания"
    description?: string; // Дополнительное описание компании
}

export interface Case {
    id: string;
    badge: string; // "Кейс"
    title: string; // "Умный AI-поиск"
    subtitle: string; // "И ответы по корпоративной документации"
    problem: string; // Текст проблемы
    solution: string; // Текст решения
    metrics: CaseMetric[]; // Массив результатов
    image?: string; // Путь к изображению (mockup/фото)
    client: CaseClient;
}

export interface Client {
    name: string;
    logo: string;
}

export interface CTAOption {
    title: string;
    description: string;
    action: string;
    actionType: 'calendly' | 'email' | 'telegram';
    link: string;
}

